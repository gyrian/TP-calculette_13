nombre_1=int(input("premierchiffre"))
nombre_2=int(input("deuxieme chiffre"))
operation=input("+"or"-"or"*"or"/")
if operation=="+":
	print(nombre_1 + nombre_2)
elif operation=="-":
	print(nombre_1-nombre_2)
elif operation=="*":
	print(nombre_1*nombre_2)
elif operation=="/":
	print(nombre_1/nombre_2)

